#!/usr/bin/env python3
"""
Generate benchmark environment fingerprint and condition support matrix.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import platform
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


def iso_now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def run_cmd(command: str, cwd: Path) -> str:
    proc = subprocess.run(
        command,
        cwd=str(cwd),
        shell=True,
        capture_output=True,
        text=True,
    )
    if proc.returncode != 0:
        return ""
    return (proc.stdout or "").strip()


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate benchmark environment metadata.")
    parser.add_argument("--markdown-out", default="Docs/Benchmarks/BENCHMARK_ENVIRONMENT.md")
    parser.add_argument("--json-out", default="Docs/Benchmarks/benchmark_environment.json")
    args = parser.parse_args()

    repo_root = Path(__file__).resolve().parents[1]
    now = iso_now()

    git_sha = run_cmd("git rev-parse HEAD", repo_root)
    git_branch = run_cmd("git rev-parse --abbrev-ref HEAD", repo_root)
    host_name = platform.node() or run_cmd("hostname", repo_root) or "unknown"
    os_name = platform.system()
    os_version = platform.version()
    os_release = platform.release()
    machine = platform.machine()
    processor = platform.processor() or run_cmd("sysctl -n machdep.cpu.brand_string", repo_root) or "unknown"
    mem_bytes_raw = run_cmd("sysctl -n hw.memsize", repo_root)
    mem_bytes = int(mem_bytes_raw) if mem_bytes_raw.isdigit() else 0
    platform_uuid_raw = run_cmd("ioreg -rd1 -c IOPlatformExpertDevice", repo_root)
    platform_uuid = "unknown"
    for line in platform_uuid_raw.splitlines():
        if "IOPlatformUUID" in line:
            parts = line.split("=")
            if len(parts) >= 2:
                platform_uuid = parts[1].strip().strip('"')
                break

    if platform_uuid == "unknown":
        seed = f"{host_name}|{os_name}|{os_release}|{machine}|{processor}"
        platform_uuid = hashlib.sha256(seed.encode("utf-8")).hexdigest()[:16]

    device_id = hashlib.sha256(f"{platform_uuid}|{machine}".encode("utf-8")).hexdigest()[:20]

    condition_support = [
        {
            "toggle": "mvcc",
            "on_supported": True,
            "off_supported": True,
            "notes": "Runtime toggle via BlazeDBClient.setMVCCEnabled.",
        },
        {
            "toggle": "wal",
            "on_supported": True,
            "off_supported": False,
            "notes": "WAL is always on in current core engine path.",
        },
        {
            "toggle": "encryption",
            "on_supported": True,
            "off_supported": False,
            "notes": "Encryption is always on in current core engine path.",
        },
    ]

    payload: dict[str, Any] = {
        "generated_at_utc": now,
        "run_fingerprint": {
            "device_id": device_id,
            "platform_uuid_source": "IOPlatformUUID" if platform_uuid_raw else "derived_fallback",
            "host_name": host_name,
            "os_name": os_name,
            "os_release": os_release,
            "os_version": os_version,
            "machine_arch": machine,
            "processor": processor,
            "memory_bytes": mem_bytes,
            "git_branch": git_branch,
            "git_commit": git_sha,
        },
        "condition_support_matrix": condition_support,
    }

    markdown = f"""# Benchmark Environment

_Auto-generated by `Scripts/generate_benchmark_environment.py` on {now}._

## Run Fingerprint

| Field | Value |
|---|---|
| Device ID | `{device_id}` |
| Host | `{host_name}` |
| OS | `{os_name} {os_release}` |
| OS version | `{os_version}` |
| Architecture | `{machine}` |
| Processor | `{processor}` |
| Memory bytes | `{mem_bytes}` |
| Git branch | `{git_branch}` |
| Git commit | `{git_sha}` |

## Condition Support Matrix

| Toggle | ON supported | OFF supported | Notes |
|---|---|---|---|
"""
    for row in condition_support:
        markdown += "| {toggle} | {on} | {off} | {notes} |\n".format(
            toggle=row["toggle"],
            on="yes" if row["on_supported"] else "no",
            off="yes" if row["off_supported"] else "no",
            notes=row["notes"],
        )

    md_path = repo_root / args.markdown_out
    json_path = repo_root / args.json_out
    md_path.parent.mkdir(parents=True, exist_ok=True)
    json_path.parent.mkdir(parents=True, exist_ok=True)
    md_path.write_text(markdown, encoding="utf-8")
    json_path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")

    print(f"Generated markdown: {md_path}")
    print(f"Generated json:     {json_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
