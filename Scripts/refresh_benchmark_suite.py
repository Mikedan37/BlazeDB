def generate_power_md(now: str, power_results: list[StepResult], power_metrics: dict[str, Any]) -> str:
    lines = [
        "# Resource and Power Proxy Benchmarks",
        "",
        f"_Auto-generated on {now} by `Scripts/refresh_benchmark_suite.py`._",
        "",
        "| Benchmark | Status | Seconds | Summary |",
        "|---|---|---:|---|",
    ]
    for r in power_results:
        lines.append(f"| `{r.name}` | `{r.status}` | `{r.seconds:.3f}` | {r.summary} |")
    lines += [
        "",
        "## Extracted XCT Metrics",
        "",
        "| Step | Metric | Unit | Average | Samples |",
        "|---|---|---|---:|---:|",
    ]
    metric_rows = 0
    for step_name, payload in power_metrics.items():
        for metric in payload.get("xct_metrics", []):
            metric_rows += 1
            lines.append(
                "| `{step}` | {metric} | `{unit}` | `{avg:.3f}` | `{count}` |".format(
                    step=step_name,
                    metric=metric.get("metric", "unknown"),
                    unit=metric.get("unit", ""),
                    avg=float(metric.get("average", 0)),
                    count=int(metric.get("sample_count", 0)),
                )
            )
    if metric_rows == 0:
        lines.append("| `-` | No XCT metric lines parsed | `-` | `-` | `-` |")

    lines += [
        "",
        "## Extracted Custom Performance Numbers",
        "",
        "| Step | Metric | Value |",
        "|---|---|---:|",
    ]
    custom_rows = 0
    for step_name, payload in power_metrics.items():
        custom = payload.get("custom_metrics", {})
        for key, value in custom.items():
            custom_rows += 1
            display = f"{value:.3f}" if isinstance(value, float) else str(value)
            lines.append(f"| `{step_name}` | `{key}` | `{display}` |")
    if custom_rows == 0:
        lines.append("| `-` | No custom benchmark print metrics parsed | `-` |")
    lines += [
        "",
        "## Notes",
        "",
        "- These are resource/power proxy runs from performance tests that instrument clock/CPU/memory/storage metrics.",
        "- CLI `swift test` exposes wall-clock summaries; for full Xcode metric distributions use result bundles in Xcode/CI.",
        "",
    ]
    return "\n".join(lines)


