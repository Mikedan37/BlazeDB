#!/usr/bin/env python3
"""
Run and refresh the full BlazeDB benchmark documentation suite.

This orchestrator:
1) Runs core benchmark generators (results, limits, sqlite comparison, latency)
2) Runs GC-focused benchmark tests
3) Runs resource/power-proxy benchmark tests
4) Writes live status updates while running
5) Emits consolidated benchmark docs and machine-readable JSON
"""

from __future__ import annotations

import argparse
import json
import re
import subprocess
import sys
import time
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


@dataclass
class StepResult:
    name: str
    command: str
    status: str  # pending|running|pass|fail|unavailable
    seconds: float = 0.0
    summary: str = ""
    log_file: str = ""


def iso_now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def read_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def summarize_output(output: str, returncode: int) -> tuple[str, str]:
    no_matching = "No matching test cases were run" in output
    distributed_unavailable = "require distributed build" in output.lower()
    if no_matching or distributed_unavailable:
        status = "unavailable"
    elif returncode == 0:
        status = "pass"
    else:
        status = "fail"

    executed = re.search(r"Executed\s+\d+\s+tests?,\s+with\s+\d+\s+failures?", output)
    if executed:
        summary = executed.group(0)
    elif no_matching:
        summary = "No matching test cases were run"
    elif distributed_unavailable:
        summary = "Unavailable in active core-only build"
    elif returncode == 0:
        summary = "Completed successfully"
    else:
        summary = "Command failed"

    return status, summary


def render_status_md(started_at: str, steps: list[StepResult]) -> str:
    lines = [
        "# Benchmark Refresh Status",
        "",
        f"_Live status generated by `Scripts/refresh_benchmark_suite.py`._",
        "",
        f"- Started at: `{started_at}`",
        f"- Last update: `{iso_now()}`",
        "",
        "| Step | Status | Seconds | Summary |",
        "|---|---|---:|---|",
    ]
    for s in steps:
        sec = f"{s.seconds:.3f}" if s.seconds > 0 else "-"
        summary = s.summary or "-"
        lines.append(f"| `{s.name}` | `{s.status}` | {sec} | {summary} |")
    lines.append("")
    lines.append("## Step Logs")
    lines.append("")
    for s in steps:
        if s.log_file:
            lines.append(f"- `{s.name}`: `{s.log_file}`")
    lines.append("")
    return "\n".join(lines)


def run_step(
    step: StepResult,
    repo_root: Path,
    logs_dir: Path,
    status_md_path: Path,
    started_at: str,
    all_steps: list[StepResult],
) -> None:
    step.status = "running"
    write_text(status_md_path, render_status_md(started_at, all_steps))

    log_name = re.sub(r"[^a-z0-9]+", "-", step.name.lower()).strip("-") + ".log"
    log_path = logs_dir / log_name
    step.log_file = str(log_path.relative_to(repo_root))

    start = time.monotonic()
    proc = subprocess.run(
        step.command,
        cwd=str(repo_root),
        shell=True,
        capture_output=True,
        text=True,
    )
    elapsed = time.monotonic() - start

    output = (proc.stdout or "") + "\n" + (proc.stderr or "")
    write_text(log_path, output)

    status, summary = summarize_output(output, proc.returncode)
    step.status = status
    step.seconds = elapsed
    step.summary = summary

    write_text(status_md_path, render_status_md(started_at, all_steps))


def parse_xct_metric_lines(log_text: str) -> list[dict[str, Any]]:
    pattern = re.compile(
        r"measured \[(?P<metric>[^,\]]+), (?P<unit>[^\]]+)\] average: (?P<avg>[0-9.]+),.*?values: \[(?P<values>[^\]]+)\]",
        re.IGNORECASE,
    )
    rows: list[dict[str, Any]] = []
    for match in pattern.finditer(log_text):
        metric = match.group("metric").strip()
        unit = match.group("unit").strip()
        avg = float(match.group("avg"))
        value_tokens = [v.strip() for v in match.group("values").split(",") if v.strip()]
        values: list[float] = []
        for token in value_tokens:
            try:
                values.append(float(token))
            except ValueError:
                continue
        rows.append(
            {
                "metric": metric,
                "unit": unit,
                "average": avg,
                "samples": values,
                "sample_count": len(values),
            }
        )
    return rows


def parse_custom_perf_lines(log_text: str) -> dict[str, float | str]:
    out: dict[str, float | str] = {}

    def grab(pattern: str, key: str, as_float: bool = True) -> None:
        m = re.search(pattern, log_text)
        if not m:
            return
        out[key] = float(m.group(1)) if as_float else m.group(1)

    grab(r"Sync:\s*([0-9.]+)ms", "async_sync_ms")
    grab(r"Async:\s*([0-9.]+)ms", "async_async_ms")
    grab(r"Speedup:\s*([0-9.]+)x", "speedup_x")
    grab(r"Individual:\s*([0-9.]+)ms", "batch_individual_ms")
    grab(r"Batch:\s*([0-9.]+)ms", "batch_batch_ms")
    grab(r"Records:\s*([0-9.]+)", "combined_records")
    grab(r"Time:\s*([0-9.]+)ms", "combined_time_ms")
    grab(r"Throughput:\s*([0-9.]+)\s*ops/sec", "combined_ops_per_sec")
    grab(r"Without compression:\s*([^\n]+)", "compression_without", as_float=False)
    grab(r"With compression:\s*([^\n]+)", "compression_with", as_float=False)
    grab(r"Savings:\s*([-0-9.]+)%", "compression_savings_percent")
    return out


def collect_power_metrics(repo_root: Path, power_results: list[StepResult]) -> dict[str, Any]:
    per_step: dict[str, Any] = {}
    for step in power_results:
        if not step.log_file:
            continue
        log_path = repo_root / step.log_file
        if not log_path.exists():
            continue
        text = log_path.read_text(encoding="utf-8", errors="replace")
        per_step[step.name] = {
            "status": step.status,
            "summary": step.summary,
            "seconds": step.seconds,
            "xct_metrics": parse_xct_metric_lines(text),
            "custom_metrics": parse_custom_perf_lines(text),
        }
    return per_step


