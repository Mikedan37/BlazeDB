def generate_energy_md(now: str, power_metrics: dict[str, Any]) -> str:
    lines = [
        "# Energy and Resource Metrics",
        "",
        f"_Auto-generated on {now} by `Scripts/refresh_benchmark_suite.py`._",
        "",
        "## Raw Metrics",
        "",
        "| Step | Clock avg (s) | CPU time avg (s) | CPU cycles avg (kC) | Memory avg (kB) | Memory peak avg (kB) | Disk writes avg (kB) |",
        "|---|---:|---:|---:|---:|---:|---:|",
    ]
    any_row = False
    energy_proxy: dict[str, float] = {}
    for step_name, payload in power_metrics.items():
        metric_map = {m.get("metric"): m for m in payload.get("xct_metrics", [])}
        clock = float(metric_map.get("Clock Monotonic Time", {}).get("average", 0) or 0)
        cpu_time = float(metric_map.get("CPU Time", {}).get("average", 0) or 0)
        cpu_cycles = float(metric_map.get("CPU Cycles", {}).get("average", 0) or 0)
        mem = float(metric_map.get("Memory Physical", {}).get("average", 0) or 0)
        mem_peak = float(metric_map.get("Memory Peak Physical", {}).get("average", 0) or 0)
        disk = float(metric_map.get("Disk Logical Writes", {}).get("average", 0) or 0)
        if any([clock, cpu_time, cpu_cycles, mem, mem_peak, disk]):
            any_row = True
            lines.append(
                f"| `{step_name}` | `{clock:.3f}` | `{cpu_time:.3f}` | `{cpu_cycles:.3f}` | `{mem:.3f}` | `{mem_peak:.3f}` | `{disk:.3f}` |"
            )
            # Simple comparable proxy index (dimensionless), not joules.
            energy_proxy[step_name] = (cpu_time * 1000.0) + (cpu_cycles / 1000.0) + (mem_peak / 1024.0) + (disk / 1024.0)
    if not any_row:
        lines.append("| `-` | `0` | `0` | `0` | `0` | `0` | `0` |")

    lines += [
        "",
        "## Energy Proxy Index",
        "",
        "_Not a battery-joules measurement; use for relative comparisons on the same machine only._",
        "",
        "| Step | Energy proxy index |",
        "|---|---:|",
    ]
    if energy_proxy:
        for step_name, value in sorted(energy_proxy.items(), key=lambda x: x[1], reverse=True):
            lines.append(f"| `{step_name}` | `{value:.3f}` |")
    else:
        lines.append("| `-` | `0` |")

    lines += [
        "",
        "## Notes",
        "",
        "- This document is first-class numeric output for energy/resource reporting in CLI runs.",
        "- For true device battery/energy instrumentation, use Xcode Instruments energy profiling.",
        "",
    ]
    return "\n".join(lines)


