#!/usr/bin/env python3
"""
Run BlazeDBBenchmarks across a condition matrix and aggregate outputs.
"""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
import time
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


def iso_now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def run_one_condition(
    repo_root: Path,
    scratch_path: str,
    condition: dict[str, Any],
    docs_dir: Path,
) -> dict[str, Any]:
    condition_id = str(condition["id"])
    md_path = docs_dir / f"results_{condition_id}.md"
    json_path = docs_dir / f"results_{condition_id}.json"

    env = dict(**__import__("os").environ)
    env["BLAZEDB_BENCH_CONDITION"] = condition_id
    env["BLAZEDB_BENCH_MVCC"] = "on" if bool(condition["mvcc"]) else "off"
    env["BLAZEDB_BENCH_WAL"] = "on" if bool(condition["wal"]) else "off"
    env["BLAZEDB_BENCH_ENCRYPTION"] = "on" if bool(condition["encryption"]) else "off"
    env["BLAZEDB_BENCH_RESULTS_MD"] = str(md_path.relative_to(repo_root))
    env["BLAZEDB_BENCH_RESULTS_JSON"] = str(json_path.relative_to(repo_root))

    condition_scratch = f"{scratch_path}-{condition_id}"
    swift_flags = ""
    if not bool(condition["encryption"]):
        # Compile-time benchmark-only encryption bypass.
        swift_flags += " -Xswiftc -DBLAZEDB_BENCHMARK_NO_ENCRYPTION"
    command = f'swift run --scratch-path "{condition_scratch}"{swift_flags} BlazeDBBenchmarks'
    start = time.monotonic()
    proc = subprocess.run(
        command,
        cwd=str(repo_root),
        shell=True,
        capture_output=True,
        text=True,
        env=env,
    )
    elapsed = time.monotonic() - start
    out = (proc.stdout or "") + "\n" + (proc.stderr or "")
    status = "pass" if proc.returncode == 0 else "fail"
    summary = "Completed successfully" if proc.returncode == 0 else "Command failed"
    if "No matching test cases were run" in out:
        summary = "No matching test cases were run"
    return {
        "condition": condition,
        "status": status,
        "summary": summary,
        "seconds": elapsed,
        "command": command,
        "output_excerpt": "\n".join(out.splitlines()[-20:]),
        "results_json": str(json_path.relative_to(repo_root)),
        "results_md": str(md_path.relative_to(repo_root)),
    }


def format_matrix_md(rows: list[dict[str, Any]], generated_at: str) -> str:
    lines = [
        "# BlazeDB Benchmarks",
        "",
        f"_Auto-generated by `Scripts/run_core_benchmark_matrix.py` on {generated_at}._",
        "",
        "| Condition | Support | Benchmark | BlazeDB (ops/sec) | BlazeDB avg ms | BlazeDB p50 ms | BlazeDB p95 ms | BlazeDB p99 ms | SQLite (ops/sec) | SQLite avg ms | SQLite p50 ms | SQLite p95 ms | SQLite p99 ms | Dataset Size | Notes |",
        "|---|---|---|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|---|",
    ]
    for row in rows:
        sqlite_ops = "N/A" if row.get("sqliteOpsPerSec") is None else f"{float(row['sqliteOpsPerSec']):.0f}"
        blaze_avg = "N/A" if row.get("blazedbAvgMs") is None else f"{float(row['blazedbAvgMs']):.3f}"
        blaze_p50 = "N/A" if row.get("blazedbP50Ms") is None else f"{float(row['blazedbP50Ms']):.3f}"
        blaze_p95 = "N/A" if row.get("blazedbP95Ms") is None else f"{float(row['blazedbP95Ms']):.3f}"
        blaze_p99 = "N/A" if row.get("blazedbP99Ms") is None else f"{float(row['blazedbP99Ms']):.3f}"
        sqlite_avg = "N/A" if row.get("sqliteAvgMs") is None else f"{float(row['sqliteAvgMs']):.3f}"
        sqlite_p50 = "N/A" if row.get("sqliteP50Ms") is None else f"{float(row['sqliteP50Ms']):.3f}"
        sqlite_p95 = "N/A" if row.get("sqliteP95Ms") is None else f"{float(row['sqliteP95Ms']):.3f}"
        sqlite_p99 = "N/A" if row.get("sqliteP99Ms") is None else f"{float(row['sqliteP99Ms']):.3f}"
        lines.append(
            "| {condition} | {support} | {name} | {bops:.0f} | {bavg} | {bp50} | {bp95} | {bp99} | {sops} | {savg} | {sp50} | {sp95} | {sp99} | {size} | {notes} |".format(
                condition=row.get("condition", "unknown"),
                support=row.get("supportStatus", "unknown"),
                name=row.get("name", "unknown"),
                bops=float(row.get("blazedbOpsPerSec", 0) or 0),
                bavg=blaze_avg,
                bp50=blaze_p50,
                bp95=blaze_p95,
                bp99=blaze_p99,
                sops=sqlite_ops,
                savg=sqlite_avg,
                sp50=sqlite_p50,
                sp95=sqlite_p95,
                sp99=sqlite_p99,
                size=int(row.get("datasetSize", 0) or 0),
                notes=str(row.get("notes", "")),
            )
        )
    lines += [
        "",
        "## Condition Runs",
        "",
        "| Condition | MVCC | WAL | Encryption | Status | Seconds |",
        "|---|---|---|---|---|---:|",
    ]
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description="Run benchmark matrix and aggregate results.")
    parser.add_argument("--scratch-path", default="/tmp/blazedb-bench-scratch")
    parser.add_argument("--markdown-out", default="Docs/Benchmarks/RESULTS.md")
    parser.add_argument("--json-out", default="Docs/Benchmarks/results.json")
    parser.add_argument("--matrix-json-out", default="Docs/Benchmarks/results_matrix.json")
    args = parser.parse_args()

    repo_root = Path(__file__).resolve().parents[1]
    docs_dir = repo_root / "Docs/Benchmarks"
    docs_dir.mkdir(parents=True, exist_ok=True)

    matrix_conditions = [
        {"id": "baseline", "mvcc": True, "wal": True, "encryption": True, "run_enabled": True},
        {"id": "mvcc_off", "mvcc": False, "wal": True, "encryption": True, "run_enabled": True},
        {
            "id": "wal_off_requested",
            "mvcc": True,
            "wal": False,
            "encryption": True,
            "run_enabled": False,
            "reason": "WAL-off is not supported in current core engine; effective mode remains WAL on.",
        },
        {
            "id": "encryption_off_requested",
            "mvcc": True,
            "wal": True,
            "encryption": False,
            "run_enabled": True,
            "reason": "Runs in benchmark-only compile-time no-encryption mode.",
        },
        {
            "id": "mvcc_off_wal_off_enc_off_requested",
            "mvcc": False,
            "wal": False,
            "encryption": False,
            "run_enabled": False,
            "reason": "Combination includes unsupported WAL off toggle; published as requested-but-unavailable.",
        },
    ]

    run_rows: list[dict[str, Any]] = []
    aggregate_rows: list[dict[str, Any]] = []
    for condition in matrix_conditions:
        if bool(condition.get("run_enabled", True)):
            row = run_one_condition(
                repo_root=repo_root,
                scratch_path=args.scratch_path,
                condition=condition,
                docs_dir=docs_dir,
            )
        else:
            row = {
                "condition": condition,
                "status": "unavailable",
                "summary": condition.get("reason", "Condition not executable in current core engine."),
                "seconds": 0.0,
                "command": "not_run",
                "output_excerpt": "",
                "results_json": None,
                "results_md": None,
            }
        run_rows.append(row)
        if row.get("results_json") is None:
            continue
        result_json = repo_root / str(row["results_json"])
        if row["status"] == "pass" and result_json.exists():
            parsed = json.loads(result_json.read_text(encoding="utf-8"))
            if isinstance(parsed, list):
                aggregate_rows.extend(parsed)

    now = iso_now()
    markdown = format_matrix_md(aggregate_rows, now)

    # append per-condition execution table
    lines = [markdown]
    for run in run_rows:
        cond = run["condition"]
        lines.append(
            "| {id} | {mvcc} | {wal} | {enc} | {status} | {sec:.3f} |".format(
                id=cond["id"],
                mvcc="on" if cond["mvcc"] else "off",
                wal="on" if cond["wal"] else "off",
                enc="on" if cond["encryption"] else "off",
                status=run["status"],
                sec=float(run["seconds"]),
            )
        )
    lines.append("")

    markdown_path = repo_root / args.markdown_out
    json_path = repo_root / args.json_out
    matrix_json_path = repo_root / args.matrix_json_out
    markdown_path.parent.mkdir(parents=True, exist_ok=True)
    json_path.parent.mkdir(parents=True, exist_ok=True)
    matrix_json_path.parent.mkdir(parents=True, exist_ok=True)

    markdown_path.write_text("\n".join(lines), encoding="utf-8")
    json_path.write_text(json.dumps(aggregate_rows, indent=2) + "\n", encoding="utf-8")
    matrix_json_path.write_text(
        json.dumps(
            {
                "generated_at_utc": now,
                "conditions": matrix_conditions,
                "runs": run_rows,
                "aggregate_count": len(aggregate_rows),
            },
            indent=2,
        )
        + "\n",
        encoding="utf-8",
    )

    print(f"Generated markdown: {markdown_path}")
    print(f"Generated json:     {json_path}")
    print(f"Generated matrix:   {matrix_json_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
